	<script>
        $(function(){
        	var carousel = {
        		nbreSlide: $('.carousel-inner>div').length,
        		current: 0,
				active: null,
        		init: function(){
        			$(window).load(function(){
        				$('.carousel-inner').find('>div:first .card-text').slideDown(300, 'easeOutSine');
        				carousel.defil();
        			});

        			$('.carousel-control-next').click(function(){carousel.next(1)});
        			$('.carousel-control-prev').click(function(){carousel.prev(-1)});

        			$('.carousel').hover(function(){
        				carousel.stop_defil();
        			},
        			function(){
        				carousel.defil();
        			});

					$('.carousel-indicator>li').each(function(ind){
						$(this).click(function(){
							var diff;
							carousel.activeIndex();
							diff = ind - carousel.current;
							if(diff > 0)
								carousel.next(diff);
							else
								carousel.prev(diff);
						});
					});
        		},

        		next: function(diff = 1){
        			$('.carousel-inner').stop(false, true).animate({marginLeft: -diff*100 + '%'}, {
						duration: 900,
						easing: 'easeInOutSine',
						complete: function(){
							for(var i = 0, elt; i < diff; i++){
								elt = $(this).find('>div:first').remove();
								$(elt).find('.card-text').css({display: 'none'});
								$(this).append(elt);
							}
										
							$(this).css({marginLeft: 0}).find('>div:first .card-text').slideDown(350, 'easeOutCubic');

							$('.carousel ul>li:eq(' + carousel.current + ')').removeClass('active');
							carousel.current+=diff;
							if(carousel.current > 3)
								carousel.current = 0;

							$('.carousel ul>li:eq(' + carousel.current + ')').addClass('active');
						}
					});
        		},

        		prev: function(diff){
					for(var i = 0, elt; i > diff; i--){
						elt = $('.carousel-inner').find('>div:last').remove();
        				$('.carousel-inner').prepend(elt).css('margin-left', function(){
							return i == (diff + 1) ? diff * 100 + '%' : 'auto';
						});
					}
        			
        			$('.carousel-inner').stop(false, true).animate({marginLeft: 0}, {
        				duration: 900, 
        				easing: 'easeInOutSine',
		        		complete: function(){
		        			$(elt).next('div').find('.card-text').css({display: 'none'});
		        			$(this).find('>div:first .card-text').slideDown(350, 'easeOutCubic');

		        			$('.carousel ul>li:eq(' + carousel.current + ')').removeClass('active');
		        			carousel.current += diff;
		        			if(carousel.current < 0)
		        				carousel.current = $('.carousel ul>li').length - 1;

		        			$('.carousel ul>li:eq(' + carousel.current + ')').addClass('active');
		        		}
		        	});
        		},

        		defil: function(){
        			this.timer = window.setInterval(carousel.next, 5000);
        		},

        		stop_defil: function(){
        			window.clearInterval(this.timer);
        		},

				activeIndex: function(){
					$('.carousel-indicator>li').each(function(ind){
						if($(this).hasClass('active'))
							carousel.current = ind;
					});
				}

        	}
        	carousel.init();
	    });
    </script>
